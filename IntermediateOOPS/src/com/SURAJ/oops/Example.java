package com.SURAJ.oops;

public class Example {
	/**
	 * @param args
	 */
	public static void main(final String[] args)
	   {
	      // Create some variables.
	      final int myVar1 = 5 + 4 * 3 / 2;
	      final int myVar2 = (5 + 4) * 3 / 2;
	      final int myVar3 = (5 + 4) * (3 / 2);
	      final int myVar4 = (5 + (4 * 3)) / 2;
	      // Output the result.
	      System.out.println(
	            "MyVar1:  " + myVar1 +
	            " nMyVar2: " + myVar2 +
	            " nMyVar3: " + myVar3 +
	            " nMyVar4: " + myVar4);
	   }
}
